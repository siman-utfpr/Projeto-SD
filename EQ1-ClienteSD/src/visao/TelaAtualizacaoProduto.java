    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        String titulo = jTextFieldTitulo.getText().trim();
        String descricao = jTextArea1.getText().trim();
        float valor = Float.valueOf(jTextFieldValor.getText().trim());
        String categoria = buttonGroupCategoria.getSelection().getActionCommand();
        String flagSP = buttonGroupTipo.getSelection().getActionCommand();
        String flagVDT = buttonGroupVDT.getSelection().getActionCommand();
        this.produto.setTitulo(titulo);
        this.produto.setDescricao(descricao);
        this.produto.setValor(valor);
        this.produto.setCategoria(categoria);
        this.produto.setFlagSP(String.valueOf(flagSP).charAt(0));
        this.produto.setFlagVDT(String.valueOf(flagVDT).charAt(0));
        try {
            if (GerenciadorServicoProduto.editarProduto(this.produto)) {
                JOptionPane.showMessageDialog(null, "Editado com sucesso!");
                FabricaVisoes.esconderTela("TelaEdicaoProduto");
                FabricaVisoes.mostrarTelaHomePage();
            } else {
                JOptionPane.showMessageDialog(null, "Erro na edição!");
            }
        } catch (JSONException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro na edição!");
            // Logger.getLogger(TelaCadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro na edição!");
            //Logger.getLogger(TelaCadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

